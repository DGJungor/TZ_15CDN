 				<p>UDP协议与TCP 协议不同，是无连接状态的协议，并且UDP应用协议五花八门，差异极大，因此针对UDP Flood的防护非常困难。其防护要根据具体情况对待：判断包大小，如果是大包攻击则使用防止UDP碎片方法：根据攻击包大小设定包碎片重组大小，通常不小于1500。在极端情况下，可以考虑丢弃所有UDP碎片。攻击端口为业务端口：根据该业务UDP最大包长设置UDP最大包大小以过滤异常流量。</p>
 			</div>
 		</div>
 		<div class="types">
 			<div class="types-img">
 				<img alt="icmp" src="images/type4-icmp.png">
 			</div>
 			<div class="types-text">
 				<span>ICMP防护</span>
 				<p>ICMP Flood 的攻击原理和ACK Flood原理类似，属于流量型的攻击方式，也是利用大的流量给服务器带来较大的负载，影响服务器的正常服务。由于目前很多防火墙直接过滤ICMP报文， 因此ICMP Flood出现的频度较低。其防御方法主要是直接过滤ICMP报文。</p>
 			</div>
 		</div>
 		
 		<div class="types">
 			<div class="types-img">
 				<img alt="connection" src="images/type5-connection.png">
 			</div>
 			<div class="types-text">
 				<span>Connection Flood防护</span>
 				<p>Connection Flood是典型的并且非常的有效的利用小流量冲击大带宽网络服务的攻击方式，这种攻击方式目前已经越来越猖獗。这种攻击的原理是利用真实的IP地址向服 务器发起大量的连接，并且建立连接之后很长时间不释放，占用服务器的资源，造成服务器服务器上残余连接(WAIT状态)过多，效率降低，甚至资源耗尽，无 法响应其他客户所发起的连接。解决方法是：主动清除残余连接；对恶意连接的IP进行封禁；限制每个源IP的连接数；对特定的URL进行防护；反查Proxy后面发起HTTP Get Flood的源。</p>
 			</div>
 		</div>
 		
